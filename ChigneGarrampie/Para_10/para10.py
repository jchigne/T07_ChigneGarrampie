#Decadificador del lenguaje del 123
# 1 = El jurado nacional de elecciones
# 2 = informo, que de las 22 lsitas inscritas
# 3 = solo 11 han sido admitidas
jurado="123"

#Iteral mgs
for letra in jurado:
    if letra == "1":
        print("El jurado nacional de elecciones")
    if letra == "2":
        print("informo, que de las 22 lsitas inscritas")
    if letra == "3":
        print("solo 11 han sido admitidas")
#fin_iterar
print("Mensaje decodificado")
