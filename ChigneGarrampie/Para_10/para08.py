#Decadificador del lenguaje del ABCZYX
# A = Hola
# B = Como estas
# C = Bien y tu
# Z = No me quejo
# Y = Que tal las clases
# X = Stayin alive
sobreviviendo="ABCDZYX"

#Iteral msg
for letra in sobreviviendo:
    if letra == "A":
        print("Hola ")
    if letra == "B":
        print("Como estas")
    if letra == "C":
        print("Bien y tu")
    if letra == "Z":
        print("No me quejo")
    if letra == "Y":
        print("Que tal las clases")
    if letra == "X":
        print("Stayin alive")

#fin_iterar
print("Mensaje decodificado")
