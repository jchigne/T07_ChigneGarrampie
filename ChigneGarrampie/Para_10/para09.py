#Decadificador del lenguaje del ABC
# A = A como su kilo de uvas
# B = 7.36 casera
# C = Tanto a subido
# D = El petroleo subio :D
compras="ABCD"

#Iteral mgs
for letra in compras:
    if letra == "A":
        print("A como su kilo de uvas")
    if letra == "B":
        print("7.36 casera ")
    if letra == "C":
        print("Tanto a subido")
    if letra == "D":
        print("El petroleo subio :D")
#fin_iterar
print("Mensaje decodificado")
