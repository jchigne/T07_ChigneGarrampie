#Decadificador del lenguaje del ABCDE
# A = cual es tu nombre
# B = hola,mi nombre es
# C = Leonel Messi
# D = el jugardor de futbol?
# E = el mismo que calza y vez
Nani="ABCDE"

#Iteral mgs
for letra in Nani:
    if letra == "A":
        print("cual es tu nombre")
    if letra == "B":
        print("hola,mi nombre es ")
    if letra == "C":
        print("Leonel Messi")
    if letra == "D":
        print("el jugardor de futbol?")
    if letra == "E":
        print("el mismo que calza y vez")
#fin_iterar
print("Mensaje decodificado")
