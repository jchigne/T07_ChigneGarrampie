#Decadificador del lenguaje del XYZ
# X = Nosotros bamos
# Y = A ganar en lima
# Z = En la final de la libertadores
pasion="XYZ"

#Iteral msg
for letra in pasion:
    if letra == "X":
        print("Nosotros bamos")
    if letra == "Y":
        print("A ganar en lima ")
    if letra == "Z":
        print("En la final de la libertadores")
#fin_iterar
print("Mensaje decodificado")
