#Decadificador del lenguaje del XYZ
# X = La cuarta maravilla
# Y = del mundo es para
# Z = Machu picchu
Konichuwua="XYZ"

#Iteral mgs
for letra in Konichuwua:
    if letra == "X":
        print("La cuarta maravilla")
    if letra == "Y":
        print("del mundo es para")
    if letra == "Z":
        print("Machu picchu")
#fin_iterar
print("Mensaje decodificado")
